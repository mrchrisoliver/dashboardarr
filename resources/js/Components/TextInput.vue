<script setup>
import { onMounted, ref } from 'vue';

const model = defineModel({
    type: String,
    required: true,
});

const input = ref(null);

onMounted(() => {
    if (input.value.hasAttribute('autofocus')) {
        input.value.focus();
    }
});

defineExpose({ focus: () => input.value.focus() });
</script>

<template>
    <input
        class="rounded-lg border border-slate-700 bg-slate-800 text-slate-200 shadow-sm placeholder-slate-500 focus:border-indigo-500 focus:ring-1 focus:ring-indigo-500"
        v-model="model"
        ref="input"
    />
</template>
